function svgShapeToSVG(shape, fillColor, strokeColor, scale) {
    let svg = '<svg version="1.1" xmlns="http://www.w3.org/2000/svg" viewBox="' + shape.viewBox + '" '
        + 'width="' + shape.w * scale + '" height="' + shape.h * scale + '">';

    let path = shape.path;
    if (!Array.isArray(path)) path = [path];

    for (let i = 0; i < path.length; i++) {
        svg += '<path fill="' + fillColor + '" stroke="' + strokeColor + '" stroke-width="' + shape.strokeWidth + '" '
            + 'd="' + path[i] + '"/>';
    }

    svg += '</svg>';

    return svg;
}